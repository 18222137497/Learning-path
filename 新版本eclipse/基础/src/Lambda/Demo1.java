package Lambda;
/**
 * Lambda表达式的标准格式：
 * 	由三部分组成：
 * 		a.一些参数
 * 		b.一个箭头
 * 		c.一段代码
 * 	格式：
 * 		(参数列表)->{一些重写方法的代码};
 * 	解释说明格式:
 * 		():接口中的抽象方法的参数列表，没有参数，就空着；有参数就写出参数，多个参数使用,分割
 * 		->:传递的意思，把参数传递给方法体{}
 * 		{}:重写接口的抽象方法的方法体
 * @author 123
 *
 */
public class Demo1 {
	public static void main(String[] args) {
	new Thread(()->{
		System.out.println("使用Lambda表达式实现，多线程");
	}).start();
	//上下等价，上面是Lambda
	new Thread(new Runnable() {
		@Override
		public void run() {
			System.out.println("正常实现多线程");
		}
		
	}).start();
}
}

package xxxxx;

import java.util.Date;

public class 常规转换符 {
	public static void main(String[] args) {
		System.out.println(String.format("字母小写:%C", 'x'));
		System.out.println(String.format("数字的和：%d", 100+100));
		System.out.println(Math.PI);
		System.out.println(String.format("Π取到小数点后两位：%.2f", Math.PI));
		System.out.println(String.format("2<3的结果：%b", 2<3));
		System.out.println(String.format("230000000.1用科学记数法表示：%e",230000000.1 ));
		System.out.println(String.format("天才是由%d%%的灵感和%d%%的汗水", 1,99));
		
	}
	
	

	
	
	
}
